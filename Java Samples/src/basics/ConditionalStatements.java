package basics;

public class ConditionalStatements {
    public static void main(String[] args) {
        int a=25;
        //simple if statement
        if(a<20){
            System.out.println("a is less than 20");
        }
        //if else statement
        if (a>20) {
            System.out.println("a is greater than 20");
        }else{
            System.out.println("a is less than 20");
        }

    }
}
